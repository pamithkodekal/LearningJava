package Loops.Patterns;

public class SquareStarPattern{
    public static void main (String[] args ){
        
        System.out.println("Square Star Patterns");
        for (int i = 0 ; i < 5 ; i++){
            System.out.println();
            for (int j=0;j<5 ; j++){
                System.out.print("*");
               
            }
        }
        
    }
}

